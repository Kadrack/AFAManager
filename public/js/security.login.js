$(document).ready(function () {
    // ----------------------------------------------------------------------------------------------------------------
    // -- Evènements --------------------------------------------------------------------------------------------------
    // ----------------------------------------------------------------------------------------------------------------

    $('a.submit').click(function () {
        submitLoginForm()
        return false;
    });

    $('#login-box input').on('keyup', function (e) {
        if (e.keyCode == 13)
            submitLoginForm();
    });

    // ----------------------------------------------------------------------------------------------------------------
    // -- Fonctions ---------------------------------------------------------------------------------------------------
    // ----------------------------------------------------------------------------------------------------------------

    function submitLoginForm() {
        var form = $('#login-box form');

        form.submit();
    }
});